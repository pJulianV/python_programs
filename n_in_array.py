def comp(array1, array2):
    for n in array1:
        if n*n in array2:
            array2.remove(n*n)
            continue
        else:
            return n*n in array2

a1 = [121, 144, 19, 161, 19, 144, 19, 11]
a2 = [11*11, 121*121, 144*144, 19*19, 161*161, 19*19, 144*144, 19*19]
print(comp(a1, a2))

